void DECK_Create(Deck* d) {
    for (int i = 0; i < DECK_SIZE; ++i)
        d->cards[i] = i;
    d->index = 0;
}

void DECK_Shuffle(Deck* d) {
    for (int i = 0; i < DECK_SIZE; ++i) {
        int j = RND_Get(0, DECK_SIZE - 1);
        int swap = d->cards[j];
        d->cards[j] = d->cards[i];
        d->cards[i] = swap;
    }
}

Card DECK_Pop(Deck* d) {
    if (d->index < DECK_SIZE)
        return CARD_SET[d->cards[d->index++]];
    else
        return CARD_ERROR;
}